package ArrayList;

public class Books {

	private int id;
	private String name;
	
	public Books() {
	}
	
	public Books(String name) {
		this.name = name;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
