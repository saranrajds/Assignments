package liftsystem.Models;

public class TravelPosition {

	private int currentPosition;
	private int distinationPosition;

	public int getCurrentPosition() {
		return currentPosition;
	}

	public void setCurrentPosition(int currentPosition) {
		this.currentPosition = currentPosition;
	}

	public int getDistinationPosition() {
		return distinationPosition;
	}

	public void setDistinationPosition(int distinationPosition) {
		this.distinationPosition = distinationPosition;
	}
}
